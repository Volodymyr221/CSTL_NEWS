-- Magic-link на безкоштовному Supabase обмежений (3-4 листи/год на проєкт).
-- Простіше — пароль. Цей SQL встановлює пароль для адміна напряму у БД,
-- не потребуючи email-сервісу.
--
-- ⚠️ ЯК ВИКОРИСТАТИ:
--   1. Заміни 'твоя_пошта@gmail.com' на свою пошту яка вже у admins
--   2. Заміни 'твій_пароль_тут' на пароль який хочеш використовувати
--      (мінімум 6 символів, краще 12+, запиши у менеджер паролів)
--   3. Run у Supabase SQL Editor
--   4. Зайди в /admin.html → введи пошту + пароль → Увійти
--
-- ⚠️ ВАЖЛИВО:
--   - НЕ комічуй цей файл у git із заповненим паролем
--   - Запусти і одразу очисти SQL Editor
--   - Якщо забув пароль — запусти цей SQL ще раз з новим паролем
-- ============================================================================

-- Встановити пароль для існуючого юзера (якщо magic-link вже створив auth.users record):
UPDATE auth.users
SET
  encrypted_password = crypt('твій_пароль_тут', gen_salt('bf')),
  email_confirmed_at = COALESCE(email_confirmed_at, now()),
  updated_at = now()
WHERE email = 'твоя_пошта@gmail.com';

-- Перевірка результату — має вернути 1 рядок:
SELECT email, email_confirmed_at, updated_at
FROM auth.users
WHERE email = 'твоя_пошта@gmail.com';

